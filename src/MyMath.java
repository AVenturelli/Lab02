public class MyMath {

    static int Somma(int a, int b)
    {
        return a+b;
    }
    static int Sottrazione(int a, int b)
    {
        return a-b;
    }
    static int Moltipicazione(int a, int b)
    {
        return a*b;
    }
    static int Divisione(int a, int b)
    {
        return a/b;
    }
}

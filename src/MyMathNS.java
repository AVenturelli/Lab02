public class MyMathNS {

    public int Somma(int a, int b)
    {
        return a+b;
    }
    public int Sottrazione(int a, int b)
    {
        return a-b;
    }
    public int Moltipicazione(int a, int b)
    {
        return a*b;
    }
    public int Divisione(int a, int b)
    {
        return a/b;
    }
}
